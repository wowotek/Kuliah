public class Ordo
{
    private int baris;
    private int kolom;

    public Ordo(int baris, int kolom)
    {
        this.baris = baris;
        this.kolom = kolom;
    }

    public int getBaris()
    {
        return this.baris;
    }

    public int getKolom()
    {
        return this.kolom;
    }
}
